                                <p>(This was written in May. I even failed to post this)</br>
                                This is now my 8th week (I think) working from home due to the coronavirus and I’ve gone through my ups and downs just like a lot of people who have had to adjust to this new lifestyle.</p>
                                <p>I’m a bit late to jumping on the bandwagon and writing an article based on working from home and life during coronavirus but that’s because it has taken me these eight weeks to figure out how to live 24/7 in my house with my family and make it work for me.</p>
                                <p>At first, I was super motivated! I was doing yoga in the morning before work, running 5K on my lunch break and spending my evening doing productive freelance side work. That went well for the first two weeks. Then I skipped yoga…. then I skipped my 5k run… then instead of being productive in the evening I’d play The Sims 4 and watch Suits on Netflix simultaneously. I’d feel unaccomplished then and get into my head that I should be doing this productive thing or that productive thing when the truth was I had to find that balance. No one can be productive all of the time, especially in times like this when it is stressful on our bodies not knowing what’s going to happen next. If our subconscious and our bodies could talk they’d be asking ‘When are we going to be back in the office?’, ‘Will we catch the virus?’, ‘When can we see our friends and families again?’, ‘will we get to do all of those summer plans?’. They don’t seem like questions our bodies could ask, we’re stuck in this unknown so we’re kind of in this constant fight or flight mode because we don’t know what’s going to happen next. The lion has been lurking watching us for the past eight-plus weeks now and we don’t know when he’s going to pounce.</p>
                                <p>So, this means we have to relax. We have to show our bodies that it’s okay we aren’t going to get eaten by the lion. We’ve been staying in our homes and social distancing so we should live and not get eaten. We need to unwind and let the stress of the unknown decrease. So it’s okay to spend 4 hours playing The Sims instead of doing that project on the to-do list or it’s okay to just relax and not go for a run.</p>
                                <p>I took me a few weeks to realise this though. I was punishing myself for not doing those things that I said I would. I would feel so crappy going to bed in the evening if I didn’t achieve something that day, even though I had worked 8 hours at my day job, but because I didn’t do any of my side jobs it meant it wasn’t a successful day.</p>
                                <p>I’ve had many failures throughout these past few weeks in quarantine. I started a <a href="https://medium.com/@caoimhegmalone/who-am-i-day1-30daywritingchallenge-3ed9c88f3362" target="_blank">30-day writing challenge</a> which only got to day 4. DAY 4!! And I accepted that as a failure but then I took a step back and thought about what I had mentioned earlier in this post. We’re in the middle of a pandemic. If I don’t want to write a blog post every day then I don’t have to write a blog post everyday. I can do whatever I want. No one is asking me to do these things that I have on my to-do list except me. Learning to have work-life balance is probably the biggest thing I’ve learned from this time in isolation. It’s okay to have an evening after work to relax and unwind it just means that if I have side projects I want to do I can do them the next day. Every day doesn’t have to be go go go and that is something I am going to try and incorporate into my life post coronavirus because pre coronavirus was so busy for me I did feel on the edge of burn out sometimes. Hopefully, I can find that work-life balance and also succeed in my projects.</p>
                                

