                            <div class="post-body">
                                <p>With Apple's release of the iPhone 11 and 11 Pro this month I thought I’d dive into why I don’t see myself leaving Apple. </p>

                                <p>At 24 I am on iPhone number 4 (iPhone  5, 6, 7, XS), that’s not exactly a high number but every second year since I was 17 I bought a new iPhone and renewed my 2-year contract with Three without hesitation. I have not always paid for my bill so that might have a factor in being able to get a new iPhone every second year.</p>
                                <p>When I was 17 myself and my sister made a presentation on Keynote, sat my parents down and made points as to why they should get us an iPhone (Looking back now it makes us sound so spoiled). My parents were reluctant but we kept persisting we even changed my Mam's lock screen of her iPhone 4 to the new iPhone 5. After we hounded them for months my mam decided that we could get them. Because she was on a work contract she wasn’t paying her phone bill herself so she decided that she would pay for ours instead. She also liked the idea of us never running out of credit in an emergency because it was bill pay we could always call her. I think she paid the few hundred for each phone too. After that we would pay the €300-500 whatever it was for the phone and my mam would pay the bill. Until I got my iPhone XS. I was just graduated from college, in a full-time job and decided to buy myself a Christmas present. I paid around €600 for the phone and for the first time my phone bill myself. And ever since I have never even considered any other brand of phone.</p>
                                
                                <p>So why no matter the cost do I keep going back to Apple again and again?</p>
                                <p>I have the first generation iPad, 2nd gen Apple TV, I had gen 0 Apple Watch and last summer upgraded to gen 4, Airpods 1, I have a 2015 iMac, a dinosaur 2008 iMac (now used mainly as a photos slideshow), I have the 2012 MacBook Pro (extremely slow and only used for Netflix) and recently thanks to my way too generous boyfriend I have the 2018 MacBook Air. </p>
                                <p>This may make me sound like I’m made of money but these were all accumulated over the past 10 years. I like to save up my money to buy expensive technology and I’m an Apple Nerd so I happily give them all of my hard-earned money. </p>
                                <p>But why? Why do I give this trillion-dollar company all of my money? </p>
                                <p>Because I believe in them. I believed in Steve Jobs. He wasn’t perfect by any means but there was a method to his madness. Without him being anal about designs he wouldn’t have revolutionised certain products. Like the swivel neck iMac or the first iPad or how the floors and windows looked in his Apple Stores. I think my belief in what he built and what Apple is today is why I am so blindly devoted to the company. </p>
                                <p>If I hadn’t of been so inspired by Steve Jobs when I was 15 then I probably wouldn’t have gone on to do Computer Science in college and wouldn’t have my career today in Web Development (can you call it a career when you’re only 1 year graduated from college?). If I didn’t mess around in iMovie with my families 2008 iMac I would never have gotten into film editing and photography later on as a hobby. </p>
                                <p>Apple has made me the person that I am. No, seriously, it has. That is why I think I will never veer away from their products. Every time an Android user complains about Apples walled garden I just brush it off and continue to give my support to my favourite company. Every time someone makes a joke about the headphone jack I tell them of how AirPods are the best product that I’ve ever bought and that I’m about to buy my second pair. Every time someone gives out about the price or how Apple is late to the party with some features I know that’s it’s because perfection takes time and that I’d rather pay €1000-2000 for the aluminium to have a long-lasting laptop (my 2012 MacBook Pro is still kicking in her old age). Every time I watch a YouTuber buy a new Windows laptop because Apple took away the ports I know that in a few years those same people will be rocking USB C everything. </p>
                                <p>I believe that they thrive to make the best products that they can without forcing a project out that isn't perfect (cough cough Galaxy Fold) and they give you something that you didn't even realise that you wanted until you had it. The Floppy Disk reader under the screen. A device that allowed you to listen to 1000s of songs and fits in your pocket. A phone with no physical keyboard. A tablet which fit perfectly between your two hands. A laptop that could fit inside an a4 envelope. A watch with a computer inside. A phone with no headphone jack. Wireless earphones. A phone with 2 cameras on the back. A phone with 3 cameras on the back. And that's just the hardware. Apple has given us so many features over the years in their Software that we just wouldn't have used or known that we wanted to use them unless Apple had taken the initiative to give us those features. They know their users so well and in return have so much loyalty from their users.</p>
                                <p>So yes I am probably way too loyal to Apple but I wouldn’t have it any other way. I join countless other people who count down the days to WWDC, who sit outside Apple Stores for days waiting for the new iPhone, who save up all of their extra money to buy the newest Apple Products because we just need to have them! We're Apple fangirls/boys devoted to the company that has touched us all in different ways. The reason why I won't leave them is simple; everything just works.</p>
                                

