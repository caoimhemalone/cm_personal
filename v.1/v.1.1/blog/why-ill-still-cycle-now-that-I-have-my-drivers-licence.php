                            <div class="post-body">
                                <p>After 5 years, 3 renewed licenses, 4 tests and many shouting sessions between me and my Mam as she instructed me, I finally passed my driving test.</p>

                                <p>Well, I passed 6 months ago but never got around to writing this blog post so here we go.</p>

                                <p>Passing my driving test has been the biggest struggle in my early 20s. I started my driving lessons in late 2014 when I was 19. I hated going to lessons. I don't know if it was the instructor that I had or if it was the fear of crashing the car but every night before a lesson I wouldn't sleep. The knots in my stomach would be that bad that I would get about 4 hours sleep. Once I was actually driving I was fine but for some reason, the thought of it was nearly making me sick so in turn, I started hating driving. When my Mam would suggest I drive the family out for dinner or down to my Nanny's I would moan and say no you just drive I'm not in the mood. This was my mindset from the very start and probably the reason it took me 5 years to get my full license.</p>

                                <p>I have always been a good driver, I obey the rules of the road, I studied all of the questions you could be asked for hours and hours but I let nerves get the best of me. All of my life I have been bad at taking tests. My teachers in school would tell my Mam that I was bright and good in class but for some reason when it came to the tests I would fall short. I have been that way from day 1 of primary school up until my final year of college I just don't test well. And it's probably down to nerves. In the first driving test I did I thought I did a good job and was surprised when I failed, I blamed the instructor being in a bad mood and kept the attitude that I was a good driver so I kept driving. I got a bit of confidence and drove my family around everywhere so after a few months I decided I'd do the test again. It was a disaster. It was a sunny day so I got really warm and started sweating while I was driving so it made me even more nervous and I made a big mistake in the sweaty state I was in. There was a weird fork in the road and when the instructor said turn right I thought he meant keep right but when I realised what he meant I had to make a quick turn to follow the route he wanted. In doing so I went into 4th gear instead of 2nd gear. The car didn't cut out but it jutted. I closed my eyes for a second and I knew that that was it I had failed. After that mistake it was like the snowball effect I kept making mistake after mistake and I was counting down the minutes until it was over. That driving test really knocked my confidence and put me off driving.</p>

                                <p>A few months later I moved to America for 3 months. When I got back I didn't get into the car again until 5 months later and I didn't keep it up. My dad started working Saturday mornings so our time to go out and drive was limited and I stopped putting the effort in. Fast forward a few months and I had started my final year of college while working a part-time job so any spare time I had was reserved for eating and showering so I told my dad to cancel the insurance all together it was a waste of money.</p>

                                <p>One more move to America for three months, one degree later, 5 more birthdays and I finally passed my driving test. At the beginning of 2019, I did a few extra lessons and another test which I failed but it didn't affect me. My instructor was so angry because he knew I was a good driver and that it was just one mistake that I made that caused the outcome. So instead of my usual shy away, forget about driving and just cycle for the rest of my life I got insured on my Mam's BMW. It is such a nice car to drive and it was modern and sturdy, unlike my dad's little car. I became more confident in my driving and in September 2019 I passed my driving test. It was the greatest achievement of my life which may sound a bit drastic considering it's just driving a car, millions of people do it, but for me, it had been such a struggle, I got knocked down so many times but I finally picked myself back up and passed.</p>

                                <p>I have been driving my Mam's car for the past few months and last week I bought myself a nice 18-2 VW Polo. It's my new baby. But will I still be cycling 100KM a week? </p>

                                <p>I'm a cyclist. I cycle to and from work. I cycle to the gym. I cycle to my friend's houses. I cycle to my boyfriend's house. So now what? Will I stop cycling everywhere? </p>

                                <p>The simple answer is no.</p>

                                <p>I love cycling. I will endure the rainy, cold, windy weather of Dublin and cycle to where I need to go. I love the freedom of no traffic because I can weave in and out of the cars while I laugh at them knowing that they will be stuck in that same spot for 20 minutes with very little progress. I love the rush of pushing my muscles to get me up the hills. The euphoria felt as the hormones flow through my body. I love eating cake at the weekend without feeling guilty because I know I've cycled so much during the week. When my hands are gripping the handlebars I feel like I'm in my happy place.</p>

                                <p>How could I give that up? Yes, now that I can drive I have a lot more freedom and I love knowing that if it's pouring rain I don't have to wear a full-body astronaut suit just to stop myself from getting a cold cycling 20 minutes to the shop, so it's a win-win situation. I can finally drive but I don't have to sacrifice my love for cycling.</p>

