                <h1>How this £4.49 Amazon accessory changed my AirPods experience completely</h1>
                <p class="post-info">
                    <span class="post-author">Caoimhe Malone</span>
                    <span class="slash"></span>
                    <span class="post-date">Aug 29, 2019</span>
